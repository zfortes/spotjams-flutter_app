class Music {
    String nameMusic;
    String nameArtist;
    String urlAlbum;
    String urlAudio;
    String artist;


    void setMusic(String nameM, String nameA, String urlAlbum, String urlAudio, String artist){
        this.nameMusic = nameM;
        this.nameArtist = nameA;
        this.urlAlbum = urlAlbum;
        this.urlAudio = urlAudio;
        this.artist = artist;
    }
}